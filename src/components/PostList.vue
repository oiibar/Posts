        :key="post.id"
        @remove="$emit('remove', post)"
      />
    </div>
    <div v-else>
      <h2>No posts</h2>
    </div>
  </div>
</template>

<script>
import PostItem from "./PostItem.vue";

export default {
  components: { PostItem },
  props: {
    posts: {
      type: Array,
      required: true,
    },
  },
};
</script>

